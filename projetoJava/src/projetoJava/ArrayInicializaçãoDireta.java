package projetoJava;
public class ArrayInicializaçãoDireta {
	public static void main(String[] args) {
		int caixa[] = new int[5];
		caixa[0] = 1;
		caixa[1] = 2;
		caixa[2] = 3;
		caixa[3] = 4;
		caixa[4] = 5;
		System.out.println(caixa[0]);
		System.out.println(caixa[1]);
		System.out.println(caixa[2]);
		System.out.println(caixa[3]);
		System.out.println(caixa[4]);
	}
}